// Declareinga variable and assigning it a string value representing a day of the week
let day = "Tuesday";

// Using switch statement
switch (day) {
  case "Monday":
    console.log("Monday: Start of the week.");
    break;
  case "Tuesday":
    console.log("Tuesday: Keep the momentum going.");
    break;
  case "Wednesday":
    console.log("Wednesday: Midweek hustle.");
    break;
  case "Thursday":
    console.log("Thursday: Almost there!");
    break;
  case "Friday":
    console.log("Friday: End of the work week.");
    break;
  case "Saturday":
    console.log("Saturday: Time to relax.");
    break;
  case "Sunday":
    console.log("Sunday: Prepare for the week ahead.");
    break;
  default:
    console.log("Invalid day.");
}
